    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String Id=uniqueID.getText();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital","root","");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from newpatient where patientID='"+Id+"'");
            if(rs.next()){
                fname.setText(rs.getString(2));
                lname.setText(rs.getString(3));
                age.setText(rs.getString(4));
                maritalStatus.setText(rs.getString(5));
                gender.setText(rs.getString(6));
                date.setText(rs.getString(7));
                bgroup.setText(rs.getString(8));
                mobile.setText(rs.getString(9));
                address.setText(rs.getString(10));
                problem.setText(rs.getString(11));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Connection error" );
        }
        uniqueID.setEditable(false);
    }//GEN-LAST:event_formComponentShown

